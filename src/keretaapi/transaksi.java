    private void jum_tikKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jum_tikKeyReleased
        // TODO add your handling code here:
        
        if(jum_tik.getText().equals("")){
            total.setText("0");
           
        }else{
            
            int jumlah = Integer.parseInt(jum_tik.getText());
            int ht = Integer.parseInt(harga.getText());
            total.setText(String.valueOf(jumlah * ht));
        }
    }//GEN-LAST:event_jum_tikKeyReleased

    private void pembayaranKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pembayaranKeyReleased
        // TODO add your handling code here:
        
        if(!pembayaran.getText().equals("")){
            int pb= Integer.parseInt(pembayaran.getText());
            int kb = Integer.parseInt(total.getText());
            if(pb >= kb){
                int tot = pb - kb;
                kembali.setText(String.valueOf(tot));
            } else kembali.setText("pembayaran masih kurang");
        }
    }//GEN-LAST:event_pembayaranKeyReleased

