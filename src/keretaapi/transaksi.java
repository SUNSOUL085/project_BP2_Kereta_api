    public void setRsv(String ID){
        String sql = "SELECT reservasi.id_reservasi,jadwal.harga_tiket,gerbong.harga\n" +
                        "FROM `reservasi` JOIN jadwal ON jadwal.id_jadwal = reservasi.id_jadwal\n" +
                        "JOIN kereta ON kereta.id_kereta = jadwal.id_kereta\n" +
                        "JOIN gerbong on gerbong.id_gerbong = reservasi.id_gerbong WHERE id_reservasi = '"+ID+"'";
        try {
            st = koneksi.con.createStatement();
            rs = st.executeQuery(sql);
            if (rs.next()) {
                String id_rsv = rs.getString("id_reservasi");
                int hj = rs.getInt("harga_tiket");
                int hg = rs.getInt("harga");
                
                int h = hj + (hj*hg)/100;
                jTextField2.setText(id_rsv);
                harga.setText(String.valueOf(h));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
